    private void saveDatabtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_saveDatabtnActionPerformed

         String bookID=bkID.getText();
        String studentID=stdID.getText();
        
        
        boolean studentid = true;
        for (int i = 0; i < studentID.length(); i++) {

            if (!(studentID.charAt(i) >= '0' && studentID.charAt(i) <= '9')) {

                studentid = false;

                JOptionPane.showMessageDialog(null, "Student id is invalid");

                return;

            }

        }
        try
        {
            if(bkID.getText().equals("") || stdID.getText().equals(""))
             {
                 
             JOptionPane.showMessageDialog(null, "field must not be empty");
             
             return;
            
             }
         Connection con= ConnectionProvider.getCon();
         Statement st=con.createStatement();
         st.executeUpdate("update issuedbooks set Return_Book='YES' where Book_ID='"+bookID+"'and Student_ID='"+studentID+"'");
         JOptionPane.showMessageDialog(null, "Book Successfully Returned");
         setVisible(false);
         new returnBook().setVisible(true);
        }
        catch(Exception e)
        {
            JOptionPane.showMessageDialog(null, "Connection Error");
        }
        
    }//GEN-LAST:event_saveDatabtnActionPerformed

