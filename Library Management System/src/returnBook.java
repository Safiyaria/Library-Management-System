    private void searchBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_searchBtnActionPerformed
        
        String bookID=bkID.getText();
        String studentID=stdID.getText();
        try
        {
             
             
         Connection con= ConnectionProvider.getCon();
         Statement st=con.createStatement();
         ResultSet rs=st.executeQuery("Select *from issuedbooks where Book_ID='"+bookID+"'and Student_ID='"+studentID+"'");
         if(rs.next())
         {
             issueDate.setText(rs.getString(3));
             returnDate.setText(rs.getString(4));
             bkID.setEditable(false);
             stdID.setEditable(false);
             
         }
         else
         {
           
            JOptionPane.showMessageDialog(null, "Incorrect Student ID or Incorrect Book ID");
            setVisible(false);
            new returnBook().setVisible(true);
            
         }
        }
        catch(Exception e)
        {
           JOptionPane.showMessageDialog(null, "Connection Error"); 
        }
        
    }//GEN-LAST:event_searchBtnActionPerformed

